//* ASSIGNMENT OPERATOR
let r = 5;

//* ADDITION ASSIGNMENT
r += 1; //? r = r + 1

//* SUBTRACTION ASSIGNMENT
r -= 1; //? r = r - 1

//* MULTIPLICATION ASSIGNMENT
r *= 1; //? r = r * 1

//* DIVISION ASSIGNMENT
r /= 1; //? r = r / 1

//* REMAINDER (modulus) ASSIGNMENT
r %= 1; //? r = r % 1
//! This will only show you what is left after you divide.

//* EXPONENTIAL ASSIGNMENT
r **= 1; //? r = r ** 1