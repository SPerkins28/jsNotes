//* EQUAL
console.log('3' == 3); //! true
console.log(3 == 3); //! true
console.log('3' == '3'); //! true

//* STRICT EQUAL
console.log('3' === 3); //! false
console.log(3 === 3); //! true

//* NOT EQUAL
console.log('3' != 3); //! false beacuse we are not comparing data types

//* STRICT NOT EQUAL
console.log('3' !== 3); //! true because we are comparing data types

//* GREATER THAN
3 > 2;

//* LESS THAN
2 < 3;

//* GREATER THAN OR EQUAL TO
3 >= 2; //! Type the same way you say it!

//* LESS THAN OR EQUAL TO
2 <= 3;

//* AND
2 && 3;

//* OR
2 || 3;


